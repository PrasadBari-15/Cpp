#include<iostream>
using namespace std;
int main(){
    int x=10;
    int y=2;
    cout<<"Adition is:"<<x+y<<endl;
    cout<<"Substraction is:"<<x-y<<endl;
    cout<<"Multiplication is:"<<x*y<<endl;
    cout<<"Division is:"<<x/y<<endl;
    //increment
    cout<<++x;      //preincrement
    cout<<endl<<x++;   //postincrement
    cout<<endl<<x;

    //decrement
    cout<<endl<<--y;
    cout<<endl<<y--;
    cout<<endl<<y;

    
}